package Entite;

/**
 *
 * @author admin
 */
public class CPresentation {
    
    private int code;
    private String label;
    
    public CPresentation(int code, String label) {
        this.setCode (code);
        this.setLabel (label);
    }
    
    public CPresentation() {
        
    }
    
    /**
     * @return the code
     */
    public int getCode() {
        return code;
    }

    /**
     * @param code the code to set
     */
    public void setCode(int code) {
        this.code = code;
    }

    /**
     * @return the label
     */
    public String getLabel() {
        return label;
    }

    /**
     * @param label the label to set
     */
    public void setLabel(String label) {
        this.label = label;
    }
   
}
